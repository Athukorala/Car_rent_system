    private void loadVehicleBrand(){
        try{
            ArrayList<VehicleBrandDTO> vehicleBrandList = ctrlBrand.getAllBrandName();
            for(int i=0;i < vehicleBrandList.size();i++){
                VehicleBrandDTO vehicleBrand=vehicleBrandList.get(i);
                vehicleBrandComboBox.addItem(vehicleBrand.getVehicleBrandName());
            } 
        } catch (Exception ex) {
            Logger.getLogger(AvailableCars.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadVehicleType() {
        String brandID = (String) txtbrand.getText();
        try {
            VehicleBrandDTO vb = ctrlBrand.search(brandID);
            if(vb==null){
                ArrayList<VehicleTypeDTO> vehicleTypeList = ctrlType.nameGetAll(brandID);
                DefaultTableModel dtm = (DefaultTableModel) vehicleTypeTable.getModel();
                dtm.setRowCount(0);
                for(VehicleTypeDTO vt1 : vehicleTypeList){
                    Object[] rowData = {vt1.getModelName(),vt1.getRegNo(),vt1.getYear(),vt1.getVehicleColor(),vt1.getSeatCapacity(),vt1.getVehicleFuelType(),vt1.getPrice(),vt1.getVehicleStatus()};
                    dtm.addRow(rowData);
                }
            }else{
            }
        } catch (Exception ex) {
            Logger .getLogger(AddVehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
