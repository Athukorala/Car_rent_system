    private void btnAddVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddVehicleActionPerformed
        VehicleTypeDTO vehicleType = new VehicleTypeDTO(
            txtid.getText(),
            txtbrand.getText(),
            txtModelName.getText(),
            txtReg.getText(),
            Integer.parseInt(txtYear.getText()),
            txtColor.getText(),
            Integer.parseInt(txtSeat.getText()),
            txtFuel.getText(),
            Double.parseDouble(txtPrice.getText()),
            txtStatus.getText());
        try{
                    boolean result = ctrlType.add(vehicleType);
                    if (result){
                        JOptionPane.showMessageDialog(this, "Vehicle Add Successfully...");
                    }else{
                        JOptionPane.showMessageDialog(this, "Vehicle Add Failed...");
                    }
        } catch (Exception ex) {
            Logger.getLogger(AddVehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
        loadVehicleType();
    }//GEN-LAST:event_btnAddVehicleActionPerformed

